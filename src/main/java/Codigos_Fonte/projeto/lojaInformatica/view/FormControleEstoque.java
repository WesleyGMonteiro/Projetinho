    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
         //Botão para pesquisar
        String nome = "%" + txtDescricao.getText() + "%";

        ProdutoDAO dao = new ProdutoDAO();

        List<Produto> lista = dao.buscaProdutosPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tbControleEstoque.getModel();
        dados.setNumRows(0);
        for(Produto c:lista){
            dados.addRow(new Object[]{
                c.getId(),
                c.getDescricao(),
                c.getPreco(),
                c.getQtd_estoque(),
                c.getFornecedor().getNome()
            });
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void tbControleEstoqueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbControleEstoqueMouseClicked
        // Clicar em uma venda
        
        id_produto = Integer.parseInt(tbControleEstoque.getValueAt(tbControleEstoque.getSelectedRow(), 0).toString());
        txtDescricao.setText((tbControleEstoque.getValueAt(tbControleEstoque.getSelectedRow(), 1).toString()));
        txtEstoqueAtual.setText((tbControleEstoque.getValueAt(tbControleEstoque.getSelectedRow(), 3).toString()));
        
        
        
       
    }//GEN-LAST:event_tbControleEstoqueMouseClicked

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        try {
            int qtd_estoque, qtd;
            
            qtd_estoque = Integer.parseInt(txtEstoqueAtual.getText());
            qtd =  Integer.parseInt(txtQtd.getText());
            
            qtd_novo = qtd_estoque + qtd;
            
            ProdutoDAO daoProduto = new ProdutoDAO();
            
            daoProduto.adicionarEstoque(id_produto, qtd_novo);
            
            JOptionPane.showMessageDialog(null, "Estoque do produto atualizado");
            
            new Utilitario().limpaTela(jPanel2);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

